    private func setupConstraints() {
        view.addSubview(tableView)
        tableView.snp.makeConstraints { (make) in
            make.edges.equalToSuperview()
        }
        
        view.addSubview(floatingButton)
        floatingButton.snp.makeConstraints { make in
