    func tableView(_ tableView: UITableView, commit editingStyle: UITableViewCell.EditingStyle, forRowAt indexPath: IndexPath) {
        if editingStyle == .delete {
            let alert = UIAlertController(title: "ÏÇ≠Ï†ú ÌôïÏù∏", message: "Ïù¥ ÏûëÏóÖÏùÑ Ï†ïÎßê ÏÇ≠Ï†úÌïòÏãúÍ≤†ÏäµÎãàÍπå?", preferredStyle: .alert)
            
            alert.addAction(UIAlertAction(title: "ÏÇ≠Ï†ú", style: .destructive, handler: { (_) in
                // Core DataÏóêÏÑú ÏÇ≠Ï†ú
                let taskToDelete = self.todos[indexPath.row]
                self.todoDataManager.context.delete(taskToDelete)
                
                // Core DataÏóê Î≥ÄÍ≤Ω ÏÇ¨Ìï≠ Ï†ÄÏû•
                do {
                    try self.todoDataManager.context.save()
                } catch {
                    print("Error saving Core Data changes: \(error)")
                }
                
                // Î°úÏª¨ Îç∞Ïù¥ÌÑ∞ Î∞∞Ïó¥ÏóêÏÑú ÏÇ≠Ï†úÌïòÍ≥† ÌÖåÏù¥Î∏î Î∑∞ ÏóÖÎç∞Ïù¥Ìä∏
                self.todos.remove(at: indexPath.row)
                tableView.deleteRows(at: [indexPath], with: .fade)
            }))
            
            alert.addAction(UIAlertAction(title: "Ï∑®ÏÜå", style: .cancel, handler: nil))
            
            self.present(alert, animated: true, completion: nil)
        }
    }
}


// MARK: - UITextFieldDelegate
